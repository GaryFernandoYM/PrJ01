    public void ReporteData(){                                                                                              

            modelo = new DefaultTableModel();
            modelo.addColumn("ID");
            modelo.addColumn("Nombre");
            modelo.addColumn("Marca");
            modelo.addColumn("Modelo");
            modelo.addColumn("Precio");
            modelo.addColumn("Stock");

                for (Producto pers : productoDao.reporteProductos()) {
                    Object[] fila = new Object[6];
                    fila[0] = pers.getId();
                    fila[1] = pers.getNombre();
                    fila[2] = pers.getMarca();
                    fila[3] = pers.getModelo();
                    fila[4] = pers.getPrecio();
                    fila[5] = pers.getStock();

                    modelo.addRow(fila);
                } 
                
                jTableProductos.setModel(modelo); 
    }
    
    public void LimpiarTablaProducto() {
            for (int p = 0; p < modelo.getRowCount(); p++) {
                modelo.removeRow(p);
                p = p - 1;
            }
        }
