    private void jButtonGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGrabarActionPerformed
        
                p.setNombre(v_nombre.getText());
                p.setMarca(v_marca.getText());
                p.setModelo(v_modelo.getText());
                p.setPrecio( Double.parseDouble( v_precio.getText() ) );
                p.setStock( Integer.parseInt( v_stock.getText() ) );
                
                
                if(!v_nombre.getText().equals("")&&!v_marca.getText().equals("")&&!v_modelo.getText().equals("")&&!v_precio.getText().equals("")&&!v_stock.getText().equals("")){
                    productoDao.registrarProducto(p);
                    ReporteData();
                    v_nombre.setText("");
                    v_marca.setText("");
                    v_modelo.setText("");
                    v_precio.setText("");
                    v_stock.setText("");
                    
                        JOptionPane.showMessageDialog(
                                        null,
                                        "Se registro conExito", 
                                        "Registro",
                                        JOptionPane.INFORMATION_MESSAGE);
                }else{
                    JOptionPane.showMessageDialog(
                                        null,
                                        "Se registro conExito", 
                                        "Registro",
                                        JOptionPane.INFORMATION_MESSAGE);
                }
                
                
                System.out.println("imprimiendo datos de form:"+v_nombre.getText());
                
        
    }//GEN-LAST:event_jButtonGrabarActionPerformed

